    private void calcularBinomialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularBinomialActionPerformed
        double X,N,P,Res,Comb;
        String S = nExitos.getText();
        X = Double.parseDouble(S);
        if(X<=0){
            error error1 = new error();
            error1.setVisible(true);
            nExitos.setText("");
        }
        S = nOportunidades.getText();
        N = Double.parseDouble(S);
        if(N<=0){
            error error1 = new error();
            error1.setVisible(true);
            nOportunidades.setText("");
        }
        S = PExito.getText();
        P = Double.parseDouble(S);
        if(P<=0){
            error error1 = new error();
            error1.setVisible(true);
            PExito.setText("");
        }
        double A = Math.pow(P, N);
        double B=1;
        double D=N;
        while(D!=0){
            B=B*D;
            D--;
        }
        double C=1;
        D=X;
        while(D!=0){
            C=C*D;
            D--;
        }
        D=N-X;
        double F=1;
        while(D!=0){
            F=F*D;
            D--;
        }
        Comb=B/(C*F);
        D=Math.pow(P,X);	
        P=1-P;	
        N=N-X;
        P=Math.pow(P,N);
        D=D*P;
        Res=D*Comb;
        S = Double.toString(Res);
        resultadoBinomial.setText(S);
    }//GEN-LAST:event_calcularBinomialActionPerformed

